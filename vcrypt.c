const uint32_t H[8] = {
    0x6a09e667, 0xbb67ae85, 0x3c6ef372, 0xa54ff53a,
    0x510e527f, 0x9b05688c, 0x1f83d9ab, 0x5be0cd19
};

void processBlock(const uint32_t *block, uint32_t *hash) {
    uint32_t W[64];
    uint32_t a, b, c, d, e, f, g, h, t1, t2;

    memcpy(W, block, 64);

    for (int t = 16; t < 64; t++) {
        W[t] = sigma1(W[t - 2]) + W[t - 7] + sigma0(W[t - 15]) + W[t - 16];
    }

    a = hash[0];
    b = hash[1];
    c = hash[2];
    d = hash[3];
    e = hash[4];
    f = hash[5];
    g = hash[6];
    h = hash[7];

    for (int t = 0; t < 64; t++) {
        t1 = h + Sigma1(e) + Ch(e, f, g) + K[t] + W[t];
        t2 = Sigma0(a) + Maj(a, b, c);
        h = g;
        g = f;
        f = e;
        e = d + t1;
        d = c;
        c = b;
        b = a;
        a = t1 + t2;
    }

    hash[0] += a;
    hash[1] += b;
    hash[2] += c;
    hash[3] += d;
    hash[4] += e;
    hash[5] += f;
    hash[6] += g;
    hash[7] += h;
}

void sha256(const uint8_t *message, size_t len, char *hash) {
    size_t numBlocks = (len + 8) / 64 + 1;
    uint32_t block[16];

    uint32_t hashValues[8];
    memcpy(hashValues, H, sizeof(H));

    for (size_t i = 0; i < numBlocks; i++) {
        size_t start = i * 64;
        size_t end = start + 64;
        if (end > len) {
            end = len;
        }

        memset(block, 0, sizeof(block));
        memcpy(block, message + start, end - start);

        if (i == numBlocks - 1) {
            block[(end - start) / 4] = len * 8;
        }

        processBlock(block, hashValues);
    }

    for (int i = 0; i < 8; i++) {
        sprintf(hash + (i * 8), "%08x", hashValues[i]);
    }
}


